public class Fruit {
    private Long canNang;
    private String vi;
    private String mauSac;
    private Long gia;

    public Long getCanNang() {
        return canNang;
    }

    public void setCanNang(Long canNang) {
        this.canNang = canNang;
    }

    public String getVi() {
        return vi;
    }

    public void setVi(String vi) {
        this.vi = vi;
    }

    public String getMauSac() {
        return mauSac;
    }

    public void setMauSac(String mauSac) {
        this.mauSac = mauSac;
    }

    public Long getGia() {
        return gia;
    }

    public void setGia(Long gia) {
        this.gia = gia;
    }
}
